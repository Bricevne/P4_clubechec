"""Player view."""


class PlayerView:
    """Class managing players views."""

    @staticmethod
    def get_name():
        """Display name."""
        return "Name: "

    @staticmethod
    def get_surname():
        """Display surname."""
        return "Surname: "

    @staticmethod
    def get_birthdate():
        """Display birthdate."""
        return "Birthdate: "

    @staticmethod
    def get_gender():
        """Display gender."""
        return "Gender: "

    @staticmethod
    def get_rank():
        """Display rank."""
        return "Rank: "
